quiz "7.4: Points and Velocity" do
	choice_answer do
		text "For the last 3 iterations, Team Blue’s average velocity is 8, Team Gold’s is 4. Which, if any, comparison between the Blue and Gold teams is valid?"
		answer "None of the above"
		distractor "Blue has more developers than Gold"
		distractor "Blue is twice as productive as Gold"
		distractor "Blue has completed more stories than Gold"
		explanation "Recall that average velocity is a concept that is contained within the team. Comparing velocities between one team and another doesn’t make sense. A Velocity of a 4 for one team provides no detail about what a velocity of 4 for another team would be."
	end
end